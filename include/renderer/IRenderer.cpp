#include "IRenderer.h"
//---------------------------------------------------------------------------------------------------------------------
// Global
//---------------------------------------------------------------------------------------------------------------------
static IRenderer* g_pRenderer = nullptr;
Camera IRenderer::camera( glm::vec3(0.0f, 0.0f, 20.0f) );

//---------------------------------------------------------------------------------------------------------------------
// Getters & Setters
//---------------------------------------------------------------------------------------------------------------------
IRenderer* IRenderer::Get(void)
{
	return g_pRenderer;
}

//---------------------------------------------------------------------------------------------------------------------
// Public Functions
//---------------------------------------------------------------------------------------------------------------------
IRenderer::IRenderer(void)
{
	if (g_pRenderer)
	{
		delete g_pRenderer;
	}
	g_pRenderer = this;
}

IRenderer::~IRenderer(void)
{
	if (g_pRenderer == this)
	{
		g_pRenderer = nullptr;
	}
}
