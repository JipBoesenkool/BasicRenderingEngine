#include "IWindow.h"
//---------------------------------------------------------------------------------------------------------------------
// Global
//---------------------------------------------------------------------------------------------------------------------
static IWindow* g_pWindow = nullptr;

//---------------------------------------------------------------------------------------------------------------------
// Getters & Setters
//---------------------------------------------------------------------------------------------------------------------
IWindow* IWindow::Get(void)
{
	return g_pWindow;
}

//---------------------------------------------------------------------------------------------------------------------
// Public Functions
//---------------------------------------------------------------------------------------------------------------------
IWindow::IWindow(void)
{
	if (g_pWindow)
	{
		delete g_pWindow;
	}
	g_pWindow = this;
}

IWindow::~IWindow(void)
{
	if (g_pWindow == this)
	{
		g_pWindow = nullptr;
	}
}
